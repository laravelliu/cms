<?php

namespace app\modules\admin\controllers;

use app\support\filter\LoginFilter;
use yii\web\Response;

class BaseController extends \app\controllers\BaseController
{
    public function init()
    {
        parent::init(); // TODO: Change the autogenerated stub
        
    }

    /**
     * 追加行为
     * @return array
     * @author: liuFangShuo
     */
    public function appendBehaviors()
    {
        return [];
    }

    /**
     * 行为
     * @return array
     * @author: liuFangShuo
     */
    public function behaviors()
    {

        $default = [
            'login' => [
                'class' => LoginFilter::className(),
                'failUrl' => '/login'
            ]
        ];
        $append = $this->appendBehaviors();

        return array_merge($default,$append);
    }


    protected function ajaxReturn($data = array(), $code = 0, $message = '')
    {
        $data = array(
            'data' => $data,
            'code' => $code,
            'message' => $message
        );

        return \Yii::createObject([
            'class' => 'yii\web\Response',
            'format' => Response::FORMAT_JSON,
            'data' => $data
        ]);
    }
}
